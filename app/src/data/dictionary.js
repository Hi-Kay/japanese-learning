// Look up suggestions for the add-word form, both directions.
// Returns up to `limit` matches: exact matches first, then prefix matches.
export function lookupWord(query, limit = 5) {
  const q = query.trim().toLowerCase();
  if (q.length < 1) return [];
  const isAscii = /^[\u0020-\u007e]+$/.test(q);
  if (isAscii && q.length < 2) return [];

  const exact = [];
  const prefix = [];
  for (const [jp, kana, romaji, english] of DICTIONARY) {
    if (isAscii) {
      const alts = english.toLowerCase().split("/").map((s) => s.trim());
      const roma = romaji.toLowerCase();
      if (alts.some((a) => a === q) || roma === q) exact.push([jp, kana, romaji, english]);
      else if (alts.some((a) => a.startsWith(q)) || roma.startsWith(q)) prefix.push([jp, kana, romaji, english]);
    } else {
      if (jp === q || kana === q) exact.push([jp, kana, romaji, english]);
      else if (jp.startsWith(q) || kana.startsWith(q)) prefix.push([jp, kana, romaji, english]);
    }
    if (exact.length >= limit) break;
  }
  return [...exact, ...prefix].slice(0, limit);
}
